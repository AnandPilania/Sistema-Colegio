    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        Gate::authorize('haveaccess','horario-profesor.index');
        $profesor = Auth::user()->id;
        $horarios = DB::table('horarios')
            ->join('materias','horarios.horario_id', '=','materias.id')
            ->join('role_user','materias.role_user_id', '=','role_user.id')
            ->join('users','role_user.user_id', '=','users.id')
            ->select('horarios.*','materias.nombre_materia')
            ->where('users.id', '=', $profesor )
            ->orderBy('horarios.id', 'asc')
            ->paginate(6);
        return view('admin.horario-profesor.index', compact('horarios'));
    }

